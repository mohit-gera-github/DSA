import java.util.*;
public class product{
    public static void main(String args[]){
        Scanner sc=new Scanner(System.in);
        System.out.println("Enter the value of a and b :");
        int a=sc.nextInt();
        int b=sc.nextInt();
        int product=a*b;
        System.out.print("Product of a and b is :");
        System.out.print(product);
    }
}